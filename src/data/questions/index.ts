// 按模块获取题目
export const getQuestionsByModule = (module: ModuleType): Question[] => {
  return allQuestions.filter(q => q.module === module);
};

// 获取随机题目
export const getRandomQuestions = (count: number, module?: ModuleType): Question[] => {
  let questions = module ? getQuestionsByModule(module) : [...allQuestions];
  const shuffled = questions.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// 获取题目统计
export const getQuestionStats = () => {
  const modules: ModuleType[] = ['言语', '判断', '数量', '资料', '常识'];
  return modules.map(module => ({
    module,
    count: getQuestionsByModule(module).length,
  }));
};
